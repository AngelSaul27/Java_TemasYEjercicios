public class Matematicos {
    public static void main(String args[]){
        
        int valor = 2, valor2 = 4;
    
        /* SUMA */
        int rel = valor + valor2;
        /* RESTA */
        int rel2 = valor - valor2;
        /* MULTIPLICACION */
        int rel3 = valor * valor2;
        /* DIVISION */
        int rel4 = valor / valor2;
        /* MODULO */
        int rel5 = valor % valor2;
        
        System.out.println("Valor  : 2");
        System.out.println("Valor2 : 4");
        System.out.println("===============");
        
        System.out.println("SUMA DE VALORES");
        System.out.println(rel);
        
        System.out.println("RESTA DE VALORES");
        System.out.println(rel2);
        
        System.out.println("MULTIPLICACION DE VALORES");
        System.out.println(rel3);
        
        System.out.println("DIVISION DE VALORES");
        System.out.println(rel4);
        
        System.out.println("MODULO DE VALORES");
        System.out.println(rel5);
        
    }

}
